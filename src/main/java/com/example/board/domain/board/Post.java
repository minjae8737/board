package com.example.board.domain.board;

import jakarta.persistence.Entity;
import jakarta.persistence.GeneratedValue;
import jakarta.persistence.GenerationType;
import jakarta.persistence.Id;
import lombok.Data;

import java.time.LocalDateTime;

/*
create table post
     (
         id bigint generated by default as identity,
         title varchar(50),
         post_content varchar(500),
         date timestamp,
         hits integer,
         member_nickname varchar(20) not null,
         board_category varchar(20) not null,
         primary key (id),
         foreign key (member_nickname) references member(nickname),
         foreign key (board_category) references board(id)
     );
*/

@Data
@Entity
public class Post {

    @Id @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id; //글 id (PK)

    private String title; //글제목
    private String postContent; //글내용
    private LocalDateTime date; //날짜
    private int hits; //조회수
    private String memberNickname; //글쓴이 Member.nickname (FK)
    private String boardCategory;  //게시판 분류 board.id

    public Post() {
    }
}
